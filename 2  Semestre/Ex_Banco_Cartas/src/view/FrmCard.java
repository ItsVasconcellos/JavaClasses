    public void atualizaTabela(){
        tbl.setRowCount(0);
        try{
            ArrayList<Card> lista = cards.BuscarTodos();
            for(Card c: lista){
                tbl.addRow(new Object[]{c.getNumber(),c.getName(),c.getClub(),c.getNationality(),c.getLeague(),c.getOverall()});
            }
            tblClients.setModel(tbl);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Não foi possível achar nenhum card com tal nome!");
        }
    }
