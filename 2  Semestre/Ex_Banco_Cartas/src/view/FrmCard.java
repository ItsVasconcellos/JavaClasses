    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        try{
            String nome = txtCadastroNome.getText();
            String nacionalidade = txtCadastroNacionalidade.getText();
            String liga = txtCadastroLiga.getText();
            String clube = txtCadastroClube.getText();
            JOptionPane.showMessageDialog(null, clube + " . " + txtCadastroNome.getText() + " " + nacionalidade + " " + liga);
            if("".equals(nome) || "".equals(nacionalidade) || "".equals(liga) || "".equals(clube)){
                throw new StringNullException();
            }

            int numero = Integer.parseInt(txtCadastroNumero.getText());
            int overall = Integer.parseInt(txtCadastroOverall.getText());
            if(numero  <= 0 || overall <=0 ){
                throw new NumberFormatException();
            }
            cards.incluir(numero, nome, clube, nacionalidade, liga, overall);
            atualizaTabela();
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null, "Não foi possível incluir o Card");
            System.out.println("Erro ao incluir Card "  + e.toString());
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Não insira texto em campos que pedem números ou overall!");
        }
        catch(StringNullException e){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos antes de cadastrar!");
        }
    }//GEN-LAST:event_btnCadastroActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try{
            String nome = txtBuscarNome.getText();
            if("".equals(nome)){
                throw new StringNullException();
            }
            ArrayList<Card> lista = cards.BuscarPorNome(nome);
            for(Card c: lista){
                tbl.setRowCount(0);
                tbl.addRow(new Object[]{c.getNumber(),c.getName(),c.getClub(),c.getNationality(),c.getLeague(),c.getOverall()});
            }
            tblClients.setModel(tbl);
        }
        catch(StringNullException e){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos antes de Buscar!");
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

